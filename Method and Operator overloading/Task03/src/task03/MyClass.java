package task03;

public class MyClass {
    public MyClass(){
        System.out.println("This is a default constructor");
    }
    public MyClass(int a){
        System.out.println("This is an overloaded constructor with one integer argument");
    }
    public MyClass(int a , int b){
        System.out.println("This is an overloaded constructor with two integer arguments");
    }
}
